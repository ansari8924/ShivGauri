export const getProductBySku = (sku) => MOCK_PRODUCTS.find(p => p.sku === sku);
export const getProductById = (id) => MOCK_PRODUCTS.find(p => p.id === id);
export const getProductsByCategory = (category) => MOCK_PRODUCTS.filter(p => p.category === category);
export const getProductsBySubcategory = (sub) => MOCK_PRODUCTS.filter(p => p.subcategory === sub);
export const getFeaturedProducts = () => MOCK_PRODUCTS.filter(p => p.isFeatured);
export const getNewArrivals = () => MOCK_PRODUCTS.filter(p => p.isNew);
export const getBestsellers = () => MOCK_PRODUCTS.filter(p => p.isBestseller);
export const searchProducts = (query) => {
  const q = query.toLowerCase();
  return MOCK_PRODUCTS.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.description.toLowerCase().includes(q) ||
    p.fabric.toLowerCase().includes(q) ||
    p.category.toLowerCase().includes(q) ||
    p.origin.toLowerCase().includes(q)
  );
};
